char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;
	char			*new_str;

	i = 0;
	while (i < n)
	{
		if (src[i] != '\0')
		{
			dest[i] = src[i];
			i++;
		}
		else
		{
			while (i < n)
			{
				dest[i] = '\0';
				i++;
			}
		}
	}
	new_str = dest;
	return (new_str);
}
